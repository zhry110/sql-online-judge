package com.vo;

public class RankVo {
    private String name;
    private Integer accept;
    private Integer pos;

    public void setAccept(Integer accept) {
        this.accept = accept;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAccept() {
        return accept;
    }

    public String getName() {
        return name;
    }

    public void setPos(Integer pos) {
        this.pos = pos;
    }

    public Integer getPos() {
        return pos;
    }
}
